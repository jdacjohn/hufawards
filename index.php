<tr>
<td valign="top" colspan="1" align="center"><img src="images/home_01pic.jpg" width="276" height="184" alt="Home 01pic"></td>
<td valign="top" colspan="1">Presiding over [real] divorce cases on Fox TV Divorce Court, Judge Mablean 
had seen and heard it all. Issues surrounding family, especially the presence 
of a strong, loving and hard working father is very dear to her. She strongly 
believes that the positive interaction between a supportive and caring father 
and his child(ren), nurtures the inner spirit, and paves the way toward healthy 
adulthood and society. However, the courtroom was proving otherwise. 
With heart-breaking cases, feuding parents standing before her and innocent 
children as puns, Judge Mablean had seen enough. She decided that she 
was going to become a catalyst for family “dining” time. Thus, became the 
birth of Honoring Unsung Fathers.</td>
</tr>

<tr>
<td valign="top" colspan="1">	The undertaking of the awards is to identify and recognize fathers who play a 
	vital role in the development of their child(ren) lives with the intentions of reaching 
	out to families and communities to perpetuate good quality and responsible 
	attributes of African American and Latino/Hispanic fathers; to encourage and 
	promote positive fathers by spotlighting fathers who accept their role and 
	responsibility, and does it well.

	Awards are generally given in five (5) categories: Solo Warrior (single father), 
	Love Cares (married father), Fatherhood Forever (divorced father), Village Dad 
	(non-biological father) and Living Legacy (father, grandfather, great-grand, etc.)</td>
	
