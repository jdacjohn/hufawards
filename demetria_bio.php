<p> <strong>Demetria McKinney Bio Con't</strong></p>
<p>Her accomplishments read as a finely detailed map headed toward her
destiny and showing the broad spectrum of her abilities. Demetria has a
rich history with screenplay- writer and director, Tyler Perry. Ms.
McKinney's list of accomplishments includes the role of "Janine" in
Tyler Perry's TV sitcom, "House of Payne". Dee Dee embraced a vital
role in the nationally renowned stage play, "Meet the Browns". Her
journey continued with Mr. Perry on Trinity Broadcasting Network
(TBN) in a televised play production entitled "Madea's Christmas" which
resulted in high ratings. In the DVD version of Tyler Perry's "Why Did I
GetMarried", the starlet revived a significant role in the play. She also
performed as the lead character "Brandy" in "Soul Kittens Cabaret".
In 2007, McKinney starred in award winning short film, "Standing
Reign." Standing Reign was the five category award winning filmin the
Atlanta 48 Hour filmfestival (Best Film, Best Acting, Best Screenplay,
Best Delivery and Audience Award). It was also an official selection in
the TOMI Film Festival of New Orleans, the Spaghetti Junction Urban
Film Festival of Atlanta, the International Black FilmFestival of
Nashville and is ranked top twenty films in the National 48 Hour Film
Project.</p>
<p>In April 2008,Ms.McKinney and Tyler Perry's House of Payne was
nominated for the 2008 Prism Award. Ms. McKinney was nominated for
Performance in a Comedy Series and Tyler Perry's House of Payne was
nominated and won the PrismAward for Comedy SeriesMulti-Episode
Storyline. In July, Ms. McKinney starred in another award winning short
film, "Before I Wake."</p>
<p>Demetria McKinney is a radiant, beautiful, diverse talent that is surely
on the rise! Dee Dee has now stepped out in the spotlight again to present
to the world another dimension of her talent. . .singing. She is making her
world debut with her new singles, the beautiful ballad, "Stay," and the
club smashing hit, "LoveWhatcha Do." Look out for this rising talent's
albumto be released first quarter 2009.
