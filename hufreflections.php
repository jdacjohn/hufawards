<div style="display: none;"><div id="Stanley_Prince" class="popBox">
	<h3>REFLECTIONS FROM PRIOR SCHOLARSHIP RECIPIENT - STANLEY PRINCE</h3>
	
	<p>My name is Stanley Prince. I am currently a fourth year criminology major at California University Fresno.  I am a 2009  MEF  Scholarship Recipient. The Mablean Ephriam Foundation has been a blessing to me and my family since they awarded me with a scholarship. Till this very moment, I am very appreciative for the time taken out and the money spent to help me reach my goal of graduating from college and becoming a federal agent.  Over these four years, I have overcome many struggles and tribulations. I know it will be all worth it at the end.  Throughout college, God has surrounded me with great people to keep me motivated to keep moving forward to accomplish my goal.  Ms. Mablean Ephriam has been a great blessing to me. I encourage everyone I come in contact with to not only attend school but graduate just as she motivated me to do the same. Again, I would like to say thank you for your blessing on my life.  I pray that God blesses you even more to do bigger and better things.
</p>
	<p>With love and appreciation </p>

	<p><em>Stanley W. Prince Jr. </em></p>

