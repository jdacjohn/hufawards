	<h3>KEISHAN HILLIARD – 2010 NURSING SCHOLARSHIP RECIPIENT</h3>
	
	<p>I have been so grateful for the scholarship I have received from your foundation and wanting to show my appreciation, however something always comes up around HUF Awards. Unfortunately, I am working the whole Father’s Day weekend (16-18) because of a delay in the last portion of my nursing program.  The scholarship has really stood in the gap when I needed it and it’s been two years. I'm now finishing my Associates Degree Nursing program at El Camino College Compton Center and graduating next week! I'm so thankful for you and your words of encouragement. I will never forget them. Please keep in touch, I am sure I will be free at some point to volunteer and give back to your wonderful cause. Please let me know if there is anything I may do and anytime.</p>
</div></div>

