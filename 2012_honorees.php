   <p>Bishop Charles E. Blake . . . a man dedicated to serving God by serving God’s people.</p>

<p>Bishop Blake serves as Presiding Bishop and CEO of the Church of God in Christ, Inc., said to be one of the largest Protestant denominations, and the largest Pentecostal denomination in the world, with an estimated 6 million members.
</p>
<p><a id="ArtistBios" href="#BishopBlakeBio" border="0">read more about Bishop Charles E. Blake. . .</a></p>

	<!--HIDDEN DIV-->
	<div style="display: none;"><div id="BishopBlakeBio" class="popBox">
		<h3>Bishop Charles E. Blake 's Bio Continued. . .</h3>
	
		<p>As Pastor of the 24,000 plus member West Angeles Church of God in Christ, Bishop Blake takes a holistic approach to ministry.  Through West Angeles, Bishop Blake seeks to enhance the lives of his congregants, spiritually, psychologically, socially, and economically.  The over 132 ministries in operation at West Angeles are evidence of Bishop Blake’s deep commitment to the well-being of West Angeles and the surrounding communities.
</p>
		<p>He is founder and CEO of Save Africa’s Children, which has contributed to more than 200 thousand children in over 400 orphan care programs in more than 25 nations on the continent of Africa.
</p>
		<p>Bishop Blake’s passion for education is realized by his serving as founding Chairman of the C.H. Mason Theological Center, having served on the Board of Directors for The Interdenominational Theological Center, and Oral Roberts University.
</p>
		<p>He has sought to share his knowledge and wisdom by authoring two books, Encountering God and Free To Dream.  After gathering years of experience as a successful Pastor in Urban America, Bishop Blake inspires Pastors to dare to dream large through his Urban Strategies Initiatives platform and conferences.
</p>
	<p>	Bishop Blake’s love for God’s people, crosses denominational lines.  He has served on the Advisory Committee of the Pentecostal World Conference, founder and chairman of the Los Angeles Ecumenical Congress, and the Council On Foreign Relations Religious Advisory Committee.  Bishop Blake was honored to serve as one of the Los Angeles Board Members and hosts of the Azusa Centennial Celebration in 2006, and most recently appointed by President Barack Obama to the Advisory Council on Faith-Based and Neighborhood Partnerships.
</p>
		<p>A recipient of numerous awards, commendations, honorary degrees, proclamations and accolades, Bishop Blake yet walks in humility and accessibility among the people of God.  Such honors as the Salvation Army Greenlining Institute Big Heart Award, the Distinguished Leadership Award from Boston University, the NAACP Image Award, the Trumpet Award, the Los Angeles Urban League Whitney M. Young Award, the Harvard Foundation Humanitarian Medal, and the ‘Proclamation of Bishop Charles E. Blake Day’ by the Los Angeles Board of Supervisors, are just a few examples of the high esteem in which Bishop Blake is held in both the religious and secular communities.
</p>
	<p>	Bishop Blake’s dedication to the things of God begins with his being the priest of his family.  He is the loving and devoted husband to Lady Mae Lawrence Blake, the father of three adult children and grandfather of eight.
</p>
	<p>	Bishop, Pastor, Leader, Humanitarian, Husband, Father, Grandfather . . . All of these roles exemplify Bishop Blake’s deep personal relationship with God and commitment to God’s people.

