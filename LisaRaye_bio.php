<p> <strong>LisaRay Bio Con't</strong></p>
<p>In television, she has appeared in episodes of <em><strong>The Parent ‘Hood</strong></em> and <em><strong>In the House</strong></em> 
	and was one of nine struggling actors featured on Bravo’s <em><strong>The It Factor</strong></em> before she landed her starring role 
	on <em><strong>All of Us</strong></em>.  Voted Black Men Magazine’s “Sexiest Woman of the Year” for two years in a row (an honor no other star has achieved in the history of the publication) the hazel-eyed beauty was also voted top five Rising Female Stars by Ebony Magazine.  
</p>
<p>It wasn’t until LisaRaye married the Premier of the Turks & Caicos in 2006, that she went from living a charmed Hollywood life to the life of a princess… or, rather, the First Lady of a nation. There were mansions, private jets and visits with the Queen of England.  There were butlers, maids, and servants … and while she never completely gave up the Hollywood casting calls, she had duties to perform as First Lady. But then rumors of infidelity and sandal began to swirl around her husband, and it all came crashing down.  Confusion, pain and tears followed … and now there is no marriage, no private jet … and no starring role … but LisaRaye is not one to look back, or sit back and feel sorry for herself.
</p>
<p>The daughter of a successful businessman and a mother who modeled professionally, LisaRaye inherited a head for business and a natural beauty for the camera. The multi-faceted actress is a positive role model for her community. Along with actress Jasmine Guy, she co-produced the first Turks and Caicos Film Festival in October 2006. LisaRaye is a proud mother to her daughter, Kai and extremely passionate about reaching out to girls ages 13-15 to help them be aware of the possibilities of life and give them confidence and direction. She created the Miss RayeDiant Jewels teen pageant, where every girl is a winner and a precious Jewel. 
</p>

