	<span>CounterPoint - featuring Gip Noble, Stoney Dixon and Louis Van Taylor</span>


<p>CounterPoint is a collection of veteran Jazz and R&amp;B musicians based in Southern California whose
individual
members have toured and recorded extensively with notables such as  Quincy Jones, Ray Charles, Patti Austin, Etta
James, Stevie Wonder, Solomon Burke, Earth Wind &amp; Fire, Nancy Wilson and a host of others. </p>
<p>Pianist Eddie "Gip" Noble, who wrote the Teddy Pendergrass smash hit, Love TKO, fronts the band.
Together with saxophone great Louis Van Taylor, bassist and musical director Stoney Dixon, David Scott on guitar
and Tyrone Flemming on drums, CounterPoint captures that historic jazz-funk fusion sound, updated with
contemporary embellishments. The result is frisky, funky beats masterfully blended in select spots with melodic
improvisation. The energy and excitment of CounterPoint is gauranteed to put a glide in your stride and a dip in
your hip.</p>


