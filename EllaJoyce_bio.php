<p> <strong>Ella Joyce Bio Con't</strong></p>
<p>Ella works extensively in legitimate Regional Theatre and earned both the JEFF Award and the Black Theatre Alliance Award from the Goodman Theatre (Chicago) for Lynne Nottage’s "Crumbs From the Table of Joy".  Ms. Joyce world-premiered two August Wilson plays directed by Lloyd Richards - "Two Trains Running" and "King Hedley II", and performed in "Fences" at the The National Black Theatre Festival, where she is a regular honored performer since 1993. Some of her Urban Theatre credits include "Willie & Esther", "Louis & Ophelia", "Brothers, Sisters, Husbands & Wives",  Medea & The Doll, and "Don’t Get God Started!" starring the Winans.  She occasionally appears in YYP Productions of  "The Vagina Monologues" by Eve Ensler, and "The Pocketbook Monologues" by Sharon McGhee.  Books on tape include Ron Milner’s "Ruby McCollum Story" portraying the voice of Zora Neal Hurston.  
</p>
<p>Many Guest star TV appearances include Mama Pearl on "Eve", "The Jamie Foxx Show", "In The House",  and  the Dean of NYU on "Seinfeld".
</p>
<p>Ella currently tours her critically acclaimed one-woman play "A Rose Among Thorns"- a Tribute to Rosa Parks, and has traveled  to 25 cities over the past 3 years, playing for churches, schools, theatres, and special events  This NAACP nominated production has received wonderful reviews, commentaries, standing ovations, awards and plaques in various cities, also receiving the Key to the City in Ft. Lauderdale.  It was an honor to have Judge Mablean attend one of Ms. Joyce’s first performances a few years ago. It continues to be booked beyond Black History Month throughout the year. For Booking Info, please visit the Calendar at <a href="www.aRoseAmongThorns.com" target="_blank">www.aRoseAmongThorns.com</a>,  or  <a href="www.EllaJoyce.com" target="_blank">www.EllaJoyce.com</a>.
</p>
