	<h3><a name="#">General Information</a></h3>


<p>			<p>	Scholarships will be awarded to three (3) 2012 graduating high school seniors to assist 
		them in their pursuit of a college education.  In addition, MEF will award  
		scholarships to persons between the ages of 25 to 40 who delayed their college education, 
		for personal or financial reasons, and have now decided to pursue a college degree.  
		We believe awarding college scholarships to these adults addresses our mission to economically 
		strengthen our communities.  </p>
	<p>
	Special consideration will be given to those students pursuing a career in the health sciences, 
	pharmaceutical science, technology, entertainment (film, television), insurance, automotive, 
	environmental science, banking and law.</p>
	<p>
	The scholarship program is open to persons residing throughout greater Los Angeles County, 
	with specific emphasis  on the underserved communities of Lynwood, Compton,  Carson, Gardena,  
	Hawthorne and, Lawndale.</p>

	<h3><a name="#">MABLE EPHRIAM NURSING SCHOLARSHIP</a></h3>


	<p>

		A special scholarship will be awarded to a student pursuing a career in nursing. 
		The Nursing scholarship will be awarded in memory of Judge Mableanâ€™s mother, Mable Ephriam, who died on February 9, 2010.  
		She was a nurse by vocation.
	</p>

	<p>This Scholarship will be awarded to any person between the ages of 18 years and 35 years, a 2012 High School Graduate or a 
		Continuing College Student, who is pursuing a degree in nursing and has completed at least one year in a nursing program.  The applicant must have at least a 2.0 cumulative GPA,
		 whether high school graduate or current college student.  The Applicant must display compassion for the sick and elderly 
		and must have demonstrated the same, either by providing care for a relative, friend or by volunteer work in a hospital,
		 a residential care facility,  convalescent home, or skilled nursing facility. 
	</p>

	<p>The Scholarship will be ongoing and available to the recipient  each year of enrollment until the completion
		of his/her nursing degree as long as the recipient meets the qualifications outlined above.
	</p>

