		<h3>Elaine Gibbs Bio Continued. . .</h3>
<p>	Elaine has been very busy since X Factor writing, producing and performing for private clients and public figures, 
	including opening the 2011 Congressional Session at the State House in Sacramento as guest artist for Assemblyman 
	Mike Davis; performing for the 2011 Annual Living Legends Award for Black History Month; and serenading 
	Senate President Darrell Steinberg for the <a href="http://www.youtube.com/watch?v=bDlvcxoRjO8" target="_blank">Special Needs 6th Annual Evening Under The Stars</a>, honoring him as a champion for the autism community, which is a charity organization Elaine has been committed to for many years.
</p><p>	Elaine lent her heartfelt and emotional voice to a music video for TV1’s Black History Month called "<a href="http://www.youtube.com/watch?v=mjkdfXKH9TA" target="_blank">Martin Can You Hear Us</a>", designed to reconnect Americans to the importance of Dr. King’s teachings.
</p><p>	She has performed in over 25 countries, with some of the biggest names in the world of Gospel and R&amp;B music such as the late Rev. James Cleveland, Vanessa Bell Armstrong, Tramaine Hawkins and Shirley Caesar. She has worked with Bobby Womack, Cherrelle, Miki Howard and the Gap Band. On a broader scale she has sung for Rod Stewart and performed at recent galas honoring individuals such as <a href="http://www.youtube.com/watch?v=xK3am9Kp4Do" target="_blank">Kenneth ‘Babyface’ Edmonds</a>, Angela Bassett, Stephanie Mills and Chaka Khan. 
</p><p>	A big part of Elaine’s life as a performer has included the inevitable wedding gig circuit, and she has had the distinct pleasure of working as vocal partner with one of Southern California’s elite bands for more than 15 years, Chuck Wansley After Hours. Together they’ve delighted Brides &amp; Grooms all over the world including Marcia Cross, Larry King, Brad Garrett, Lela Rachon and many others.
</p><p>	Film scores and TV tracks have also been part of Elaine’s story, including voice-overs for Disney Studios, and a collaboration duet with the late Ollie Woodson of The Temptations. She also sang for the film ‘This Christmas’ produced by and starring actor Mekhi Pfeifer, and performed live with Justin Timberlake for Victoria Secret’s TV show.
</p><p>	She released her very first R&amp;B album as a young lead singer with the group Side Effect, and Billboard magazine - the record industry’s bible, wrote that she was "a voice to be reckoned with" comparing her to such legends as Aretha Franklin, Gladys Knight and Chaka Khan.
</p><p>	Beyond Elaine’s natural gifts she has furthered her vocal training under the tutelage of Seth Riggs and Brad Chapman at The Seth Riggs Vocal Studio at UCLA. Elaine’s undying belief is that her voice was created by God for greatness, and it is with this greatness she delivers a pure emotional connection straight to the heart of anyone who listens. 
</p>
