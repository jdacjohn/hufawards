<p> <strong>Dorien Wilson Bio Con't</strong></p>
<p>Wilson has also had recurring roles on popular shows such as "Friends", "Sister-Sister", "The Steve Harvey Show", "Living Single" to name a few.  He has guest starred in over 20 primetime shows including "Martin", "Seinfeld", "The Fresh Prince", "Cold Case", and "1-800-MISSING".
</p>
<p>Some of Wilson's Film credits include "You Got Served", "King of Sorrow", "House Party 4", and "Beethoven".  Wilson is slated to shoot a new movie this summer in Louisiana entitled Cadillac Psalms.
</p>
<p>He returns to the stage (his first true love) after performing at the National Black Theatre Festival in North Carolina in the stage play "South of Where We Live" starring Tommy Ford, Wendy Raquel Robinson, Michael Beech and Denise Dowse.
</p>
<p>Wilson has just started his own Production Company with his business partner:"Amador Productions". One of his first endeavors was producing the stage play "Extremities" starring himself and Wendy Raquel Robinson which also received rave reviews from the National Black Theatre Festival and The NAACP.
</p>
<p>He is a graduate of Pacific Conservatory of Performing Arts in Santa Maria, CA and the prestigious American Conservatory Theatre in San Francisco, CA.  
</p>
<p>He is a devoted father to his two children, Sarita and Devin.</p>

