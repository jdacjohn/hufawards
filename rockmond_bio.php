<p> <strong>Rockmond Dunbar Bio Con't</strong></p>
<p>His journey began with a series regular role in Steven Spielberg’s “Earth 2”
strapped under his belt. Once Rockmond arrived on the Hollywood scene, it
was only one short month before he landed recurring roles on many TV series. To
his credit, he now has had many guest starring and recurring on such shows as
"Felicity," "The Pretender," "Good News," "Two Guys and a Girl," "G. vs. E," and
“North Shore” “Girlfriends”, “Shark” “CSI: Miami” “Grey’s Anatomy” and many
others. This talented, theatrically trained actor experienced yet another career
boost when he secured leading roles in various independent films in the films,
”Jada”, “Alien Invaders”, and “Kiss Kiss Bang Bang” with Robert Downer Jr.
Rockmond was recently seen in Tyler Perry’s film “The Family That Preys” along
side Kathy Bates and Alfre Woodard. In 2010 he will be seen in Sean Ryan’s new
television series “Terriers” on FX and also the feature film “Highland Park” with
Danny Glover.</p>

<p>Remarkably enough, Rockmond also boasts writing, producing and directing
titles to his credit. This past year Mr. Dunbar directed his first full-length feature
film, "Pastor Brown," and is currently completing writing the TV series based upon
the film. In 2007 he starred and produced the independent feature film, “Dirty
Laundry,” with Loretta Devine. Rockmond is gaining favorable notices as a
quality actor from producers, directors and audiences across the country. To
capitalize upon his success and growing recognition as a filmmaker, Rockmond
formed Flypaper Entertainment to create film and television projects.</p>
Once you experience his talents as an actor and artist you will understand why
this young man is destined for great success.
</p>
