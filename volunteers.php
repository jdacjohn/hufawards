	<h3><a name="#">Volunteers Needed</a></h3>
	<p style="width: 600px;">On June 17, 2012 the Mablean Ephriam Foundation will host its 9th Annual Honoring Unsung
	Fathers (H.U.F.) Awards Brunch.
	Due to the large scale of this event and the star power its celebrity presenters and honorable 
	guests will generate, there will be a feasible contingency of media on hand to cover the awards 
	program and taping. To accommodate the many guests, VIPs and Corporate sponsors, the 
	Mablean Ephriam Foundation is seeking professional individuals with special events and/or 
	related experience in the Los Angeles area to volunteer during the event. The following is a 
	list of assignments:</p>

	<ul>
<li>Red Carpet attendants</li>
<li>Media Check-In</li>
<li>Spotters (security checkpoints, lobby, other critical areas)</li>
<li>VIP Escort Supervisor</li>
<li>VIP Escorts</li>
<li>Personal Assistant Supervisor</li>
<li>Personal Assistant</li>
<li>Award Recipient Escorts</li>
<li>Vendor Coordinators</li>
<li>Registration Volunteers</li>
<li>Runners</li>
<li>Floaters</li>
</ul>

<p style="width:600px;">	Volunteer assignments will run from Saturday, June 16 through Sunday, June 17, 2012 (evening). 
	While some assignments will involve celebrity contact, others will be less glamorous-but still very 
	critical functions to the success of the entire event. We ask that all volunteers be prepared to assist
	with ALL duties during these two days.</p>


	
