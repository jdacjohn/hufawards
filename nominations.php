<td valign="top"><span>Solo Warrior Award... </span><br>
A single, unmarried, father , currently raising his minor (under the age of 18 years) 
child/(ren) alone, accomplishing the daily routines traditionally performed by a mother such as, 
but not limited to, washing, cooking, ironing, homework, doctor's appointment; transports child to 
and from child care; involved in school and after school activities; involved in community programs 
and/or organizations, active participant in parent-teacher programs; a father who shows love, 
compassion for his child/(ren). He must have been a father for at least three (3) years and must 
have been the primary custodial parent of his child(ren) for at least one (1) year.</td>
