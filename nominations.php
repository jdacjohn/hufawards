<tr>
<td valign="top"><img src="images/nom_box.jpg" width="93" height="110" alt="HUF Award"></td>
<td valign="top"><span>Fatherhood Forever Award... </span><br>
	This award is presented to a divorced father, who has 
	divorced his spouse (mother of his child(ren), but not his child(ren). He recognizes that fatherhood 
	continues despite the divorce. This father continues to be actively involved in the life/lives of this 
	child(ren) and does not use divorce as an excuse to neglect his child(ren); he spends quality time 
	with his child(ren) at least weekly; he is in regular communication with his child(ren) in-person, by 
	phone and mail/e-mail; he is aware of school matters, health issues and filed trips; he is involved 
	in activities surrounding the child and provides regular child support for his child(ren) and makes 
	certain that the needs of his child(ren) are met, with or without a court order.; provides emotional 
	as well as financial support; this father does not let the divorce prevent him from a meaningful, 
	loving relationship with his child(ren) and has overcome any and all obstacles which may impede 
	his relationship with his child(ren).</td>
</tr>

<tr>
<td valign="top"><img src="images/nom_box.jpg" width="93" height="110" alt="HUF Award"></td>
<td valign="top"><span>Village Dad Award... </span><br>
	A male who has no biological children. However, he has become a 
	father, either as an adoptive parent, foster parent, or, simply, has stepped into the role of "father" 
	for a child by providing the love, nurture, psychological, and financial support for a child(ren) for 
	whom he has no legal or moral obligation. He spends quality time with the child(ren), such as, taking 
	these non-biological children on outings, assisting them with homework; paying for school or college 
	tuition; involved in some community activity where he acts s as a parent for a child(ren) whose natural 
	father is absent; he understands the importance of family;, he is a positive father-figure; he lends 
	himself to help child(ren) whose father is absent, by serving as mentor, motivator, disciplinarian, 
	financial supporter, handyman, counselor, protector, etc.</td>
</tr>

<tr>
<td valign="top"><img src="images/nom_box.jpg" width="93" height="110" alt="HUF Award"></td>
<td valign="top"><span>Living Legacy Award... </span><br>
	This award is given to a father, grandfather or great-grandfather, etc. who is at least 65 years of age 
	or older; married for at least thirty-five (35) years or more, to the same woman; a father who continues 
	to demonstrate great fatherhood toward his child, children, grandchildren great-grandchildren, 
	community children; a man of good moral character; good work ethics; a role model for fathers under 
	65 years of age; provides spiritual guidance for the children in his family and the neighborhood children; 
	unselfish, giving, caring and compassionate. He must be alive. This award will not be given posthumously.
	</td>
