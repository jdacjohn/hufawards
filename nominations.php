<blockquote><p> Nominations are open to the public each year between the months of February and May. 
	Only applications of those nominees meeting the criteria within the perspective category will be accepted.
	Please make certain that your Nominee meets all of the criteria set forth in the category for which he is nominated. 
	If not, the application may be rejected or
	 placed in the category the reviewing committee deems appropriate.
</p>
<p>In all categories, Nominees should not have any history of domestic violence, spousal abuse, child abuse, parental neglect. All Nominees should be current in child support and should not have any history of non-payment of child support.
</p>
<p>All applicants must be a father of good moral character, who actively participates in the lives of his child(ren), giving their health and welfare top priority; must be a father who participates in church, school, extracurricular activities and community activities in which his child(ren) are involved; he must provide financial and emotional support for this child(ren.
</p>
<p>All nominated fathers must attend the event.  Nominators are encouraged to invite family, friends, co-workers to come 
	to event to celebrate with the nominated father.  One table of ten (12) will be reserved for each nominated 
	father and it is expected that each nominated father will fill at least one table.  Of course, you may sell more than ten tickets.
	 The nominee will be admitted to the brunch free. All others must pay. 
	You may invite as many people as you like and sell as many tickets as you like.
	 We just encourage you to do your best to fill at least one table. 
	The foundation will not pay for anyone to attend other than the nominated father.  Please go to the <a href="2012awards.php#ticketinfo">Ticket Reservation Page</a> for ticket prices.
 </p>
<p>Please read the nomination requirements carefully for each category and make certain that your 
	nominee meets the requirements for that category.
 </p>
<p>We do not give posthumous awards.</p>


 







