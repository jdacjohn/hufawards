<td valign="top"><span>Love Cares Award... </span><br>A married father, living with his wife and at least one of his biological child(ren) who is under 
the age of 18 years old; has been a father for at least five (5) years; has demonstrated great 
responsibility and care for his child(ren); a father who actively participates in the daily routines 
of child rearing, assistingthe ,other; a father who does all that he can for his in child(ren) ; 
a father who encourages and shows compassion for his child(ren), not afraid to display affection 
toward his child(ren); assists them with homework; plays with them; takes them on outings; involved 
in school activities; and all aspects of child rearing.</td>
