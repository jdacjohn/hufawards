<td valign="top" valign="top" colspan="3"><strong><font style="font-size: 14px;">Kelly Price</font></strong> - Singer
	
<p>	Born into a family of singers and preachers Kelly Cherelle Price entered the world on April 4, 1973 as the 2nd of 
	3 daughters to Rev Joseph and Mrs. Claudia Price. Kelly's parents who were both Pentecostal preachers were also 
	both singers however even to her mother's surprise she began to show signs of a musically inclined ear at 9 months 
	old when she could mimic the melodies she heard in church and those she sang to her at home by repeating them with 
	the sound of a hum.
	
<p>	Public performances began in church at the age of two with the children's choir with solos beginning by age 3. By age 6 Kelly was far advanced becoming the youngest member of the junior choir for teenaged kids. By age 7 Kelly had written her 1st song and by 9 years old she was singing lead vocals in the local church's adult choir as well as the regional, state and national convention choirs before the 10's of thousands of parishioners who attended the church's worldwide conventions. In elementary school Kelly became the go to girl when a voice was needed, astounding her teachers who couldn't believe the big voice that came from this young child. This resulting in her teachers and the other faculty members crowning her with the nickname "Little Mahalia".
	
	
