<p> <strong>Judge Greg Mathis Bio Con't</strong></p>
<p>Mathis received his Bachelor of Science degree from Eastern Michigan University in 1983 and received his Juris Doctor degree from the University of Detroit School of Law in 1987. During this time and thereafter Judge Mathis continued his work in politics organizing over a dozen major campaigns resulting in the election of numerous politicians. 
</p>
<p>In his efforts to reach out to youth both in and outside of the courtroom, Judge Mathis and his wife Linda have 
	assisted thousands of youth with their non-profit agency <em>Young Adults Asserting Themselves</em> (Y.A.A.T.) an outreach initiative that provides youth ages 17-25 with career and job opportunities as well as job training and college enrollment assistance. They have also opened five non-profit preschools in the inner city of Detroit, while continuing to raise over $5 million for a variety of equal justice political and youth causes. 
</p>
<p>Mathis is dedicated to offering youth second chances and spreads his message of youth empowerment and equal 
	justice as he travels to speak to audiences all across the country. His national education and youth crusades 
	have attracted tens of thousands of parents and students throughout America. 
</p>
<p>He has received numerous awards and accolades from city, state and government officials and has been recognized for his 
	efforts by numerous newspapers, magazines and television affiliates such as ABC, CBS, NBC and CNN having appeared on 
	such shows as <em>The Tonight Show with Jay Leno, The Today Show, Larry King Live</em> and <em>The Ellen DeGeneres Show</em>. 
</p>
<p>His inspirational story of a young man who rose from delinquent to Michigan District Court Judge is chronicled in his 
	autobiography Inner City Miracle. Mathis is married and the proud father of four children.
</p>
