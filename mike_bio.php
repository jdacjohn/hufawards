<p> <strong>Mike Phillips Bio Con't</strong></p>

<p>Like so many top talents, Mike began his love affair with music very early in life, experimenting 
	with a list of instruments, before settling on the saxophone. The ambitious student diligently 
	practiced his craft, wood shedding with a vengeance. By the time he reached 16 he knew he wanted to do it for a living.
</p>
<p>In 1993, Phillips made his presence felt with his now legendary debut performance at Wilson’s, a popular 
	NYC jazz and R&B hotspot. Invited by a friend playing with the house band, Mike unexpectedly took the 
	stage and to the surprise of the bandleader, started riffing before anyone asked him to sit in. Gradually,
	 the bewildered looks of the other players (and audience) morphed into amazed smiles. But, no smile was bigger 
	than his own when musical icon Stevie Wonder, who was there, subsequently offered Mike a job playing with him 
	at Nelson Mandela’s 90th birthday celebration in South Africa. And Mike continues to play with him, most recently
	 in Japan and a whirlwind tour of the Netherlands.
</p>
<p>Phillips has been entertaining audiences with his genre-blending sax play and the help of notable artists 
	from an array of musical arenas. They include contemporary jazz luminaries the likes of Wayman Tisdale, 
	Jonathan Butler and Jeff Lorber to R&B superstars Babyface, Brian McKnight and Jill Scott. His debut album, 
	You Have Reached Mike Phillips entered Billboards Contemporary Jazz chart at #4 while his recently released 
	sophomore release, Uncommon Denominator, has already reached #3.
</p>
<p>Via Mike's guest performances on Hidden Beach's Experience: Jill Scott 826+ and the chart-topping hip hop/jazz 
	effort Unwrapped volumes among other projects, music lovers have enjoyed a small sampling of Phillips's skills. 
	Skills that earn him the distinction of being the first and only non-athlete endorsed by Brand Jordan, joining 
	such fellow members as Derek Jeter and Carmelo Anthony.
</p>
<p>In addition to a multitude of personal appearances including Pop-Jazz concerts and events, a host of jazz festivals,
	 the Capital One Bowl, Phoenix Open, Black Ski Summit, and the McDonald’s Williams Sisters Tour, Mike can also 
	be seen on his BET J lifestyle show Mike’s World.
</p>
