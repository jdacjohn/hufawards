  <h2>Tanika Ray</h2>
	<p>Entertainment journalist & TV personality Tanika Ray, known for her quick and witty on‐air banter, is truly a renaissance woman. Not
	only does she deftly handle the multi‐layered responsibility of hosting and producing a live red carpet, but she has a unique ability to
	connect with guests one‐on‐one and can hold her own whenever appearing as a panelist, with a group or in a talk show format. A pop
	culture expert, Tanika often appears as a guest herself on popular news shows where she sassily dishes about currently trending topics.</p>
<p>	Most recently seen on HGTV where she hosted two of their most exciting shows – Season 6 of <strong>DESIGN STAR</strong>, the fast‐paced interior
	design reality competition, and Season 1 of <strong>HGTV’d</strong>, their fun makeover show that criss‐crossed the country surprising viewers with a
	new room by their favorite designer – Tanika also blogs about hair, interior design, manifesting your dreams and keeping your soul
	happy on her new website <a href="http://www.mindbodycasa.com" target="_blank">mindbodycasa.com</a>. Always entertaining and accessible, Tanika has become an audience favorite and
	continues to build a strong following in world social media outlets like Facebook, Twitter and Pinterest, where she chooses to post
	photos, notes and quotes that spread positive energy and focus on elevating your mind, loving your body and beautifying your home.</p>
	
	<p><a id="ArtistBios" href="#TanikaRayBio" border="0">read more about Tanika Ray. . .</a></p>
	
