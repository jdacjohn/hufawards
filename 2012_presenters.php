  <h2>Dawnn Lewis</h2>
	<p>Dawnn Lewis is a multitalented and multifaceted individual.  Perhaps best known for her roles on the hit TV shows A DIFFERENT WORLD (composer of shows theme song) and HANGIN’ WITH MR. COOPER, she is also a Grammy Award-winning singer, a multiple ASCAP & BMI Award-winning songwriter, a film actor I’M GONNA GIT U SUCKA, DREAMGIRLS .  A TV creator/producer (BLACK JAQ) for ABC.   She gives voice to the title character in the animated feature film DINO MOM!  She just finished principal photography on the new  film “LET IT SHINE”” co-starring with Courtney B Vance & Tyler Williams,  while filming guest spots on  CBS CSI: MIAMI,  FOX BREAKING IN  &  BET REED BETWEEN THE LINES.   She also continues her recurring role on the ABC Family’s The Secret Life of An American Teenager and until recently the CW Network series One Tree Hill.   She was featured in the Warner Bros. feature film THE PREACHERS KID, & the Fox animated film FUTURAMA: INTO THE WILD GREEN YONDER..  Dawnn also starred in the 2009 NYC City Center “Encore’s” presentation of THE WIZ  as Addapearle. 
