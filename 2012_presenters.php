	<div style="display: none;"><div id="TanikaRayBio" class="popBox">
		<h3>Tanika Ray's Bio Continued. . .</h3>
		
		
	
	
	
	 <p>	While Tanika developed her hosting skills for many years on shows produced for CBS Saturday Mornings, Lifetime, and Soapnet, her
		career really blossomed when she was appointed the New York correspondent for <strong>EXTRA!</strong>, NBC’s syndicated, nightly entertainment
		magazine program. She quickly became one of Gotham’s favorite entertainment journalists; even Barbara Walters couldn’t resist and
		invited Tanika to fill the famous “fourth chair”– not once but twice – as a guest host on ABC’s morning talkfest, <strong>THE VIEW</strong>. Tanika has
		also appeared as an expert panelist on shows such as <strong>CNN’s LARRY KING LIVE</strong>, CNN’s Headline News program <strong>SHOWBIZ TONIGHT</strong>,
		and <strong>NBC’s TODAY SHOW</strong>. Working as a daily correspondent and co‐host of EXTRA!’s nightly show for five years, she concurrently
		hosted <strong>EXTRA!’s WEEKEND SHOW, CW NOW,</strong> and <strong>TBS WEEKEND EXTRA!</strong>, while she was also hosting & producing <strong>EXTRA!</strong> on
		ESSENCE.com. In late 2009,Tanika left <strong>EXTRA!</strong> to pursue more diverse opportunities.</p>
	<p>	Since then, she has spread her wings as the host of two different but highly‐charged reunion specials for popular VH1 reality shows,
		<strong>FOR THE LOVE OF RAY J</strong>, Season 2 and <strong>BASKETBALL WIVES</strong>, Season 1. She also hosted two three‐hour, live red carpet specials for
		the TV Guide Network in 2010 – The OSCAR Countdown and The EMMY Countdown – and has become the go‐to host for their Big
		Movie Premiere franchise taking the reins of three one‐hour movie specials recapping and celebrating the films <strong>PRECIOUS, SEX AND
		THE CITY 2, AND EAT PRAY LOVE</strong>. 2010 hit a high note for Tanika when she was invited to appear on<strong> NBC's TODAY SHOW</strong> and the
		<strong>CBS EARLY SHOW</strong> as an expert contributor on pop culture. In February 2012, she hosted the live, global stream red carpets for Tyler
		Perry’s <strong>GOOD DEEDS</strong> film premiere and for the <strong>ESSENCE</strong> 5th Annual Black Women in Hollywood Gala at the Beverly Hills Hotel.
		Tanika is also often called upon to host and emcee fundraisers, benefit concerts and diverse special events. For the past two years, she
		has been honored to host the annual gala of <strong>BRAG (Black Retail Action Group)</strong> in New York. She also presided over the Village Care of
		New York’s “Tulips and Pansies Headdress Affair” which raises money for New Yorkers living with HIV/AIDS. Additionally, Tanika is
		involved with supporting The Jenesse Center, a domestic violence intervention program.</p>
	<p>	Born in Washington D.C. and raised in Los Angeles, Tanika was 7 when she knew she would thrive in the entertainment industry.
		Inspired by Alvin Ailey and Debbie Allen, she began her career as a dancer where she performed with top music stars like Will Smith,
		Aaliyah, and Brandy, later landed plum acting roles on shows like NYPD BLUE & LIVING SINGLE and then found her talent in hosting.
		She graduated cum laude with a Bachelors degree in Theater Arts from Spelman College in Atlanta and studied theater at New York
		University's Tisch School of the Arts. Tanika is dedicated to inspiring young women to live their dreams and follow their passions.
		Vibrant and energetic, she's a free spirit who loves the adventure life has to offer and always continues to seek new challenges.</p>

